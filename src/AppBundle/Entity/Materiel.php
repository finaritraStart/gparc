<?php

namespace AppBundle\Entity;

/**
 * Materiel
 */
class Materiel
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $nomMaterielUtiliser;

    /**
     * @var string
     */
    private $designation;

    /**
     * @var string
     */
    private $quantite;

    /**
     * @var string
     */
    private $prixMateriel;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nomMaterielUtiliser
     *
     * @param string $nomMaterielUtiliser
     *
     * @return Materiel
     */
    public function setNomMaterielUtiliser($nomMaterielUtiliser)
    {
        $this->nomMaterielUtiliser = $nomMaterielUtiliser;

        return $this;
    }

    /**
     * Get nomMaterielUtiliser
     *
     * @return string
     */
    public function getNomMaterielUtiliser()
    {
        return $this->nomMaterielUtiliser;
    }

    /**
     * Set designation
     *
     * @param string $designation
     *
     * @return Materiel
     */
    public function setDesignation($designation)
    {
        $this->designation = $designation;

        return $this;
    }

    /**
     * Get designation
     *
     * @return string
     */
    public function getDesignation()
    {
        return $this->designation;
    }

    /**
     * Set quantite
     *
     * @param string $quantite
     *
     * @return Materiel
     */
    public function setQuantite($quantite)
    {
        $this->quantite = $quantite;

        return $this;
    }

    /**
     * Get quantite
     *
     * @return string
     */
    public function getQuantite()
    {
        return $this->quantite;
    }

    /**
     * Set prixMateriel
     *
     * @param string $prixMateriel
     *
     * @return Materiel
     */
    public function setPrixMateriel($prixMateriel)
    {
        $this->prixMateriel = $prixMateriel;

        return $this;
    }

    /**
     * Get prixMateriel
     *
     * @return string
     */
    public function getPrixMateriel()
    {
        return $this->prixMateriel;
    }
}
